PREFS_STRINGS.update(ADDITIONAL_PREFS_STRINGS)

for lang in MORE:
    po_path = os.path.join(PO_DIR, f"{lang}.po")
    mo_dir  = os.path.join(LOC_DIR, lang, "LC_MESSAGES")
    mo_path = os.path.join(mo_dir, f"{DOMAIN}.mo")

    # Write .po
    content = PO_HEADER.format(domain=DOMAIN, lang=lang)
    content += f'msgid "more"\nmsgstr "{MORE[lang]}"\n\n'
    content += f'msgid "Press Shift + N to view notifications"\nmsgstr "{TOGGLE[lang]}"\n\n'
    content += f'msgid "Sleep prevented by an active process"\nmsgstr "{INHIBIT[lang]}"\n\n'
    
    # Add prefs strings
    for eng_str, lang_map in PREFS_STRINGS.items():
        escaped_eng = eng_str.replace('"', '\\"')
        escaped_trans = lang_map.get(lang, eng_str).replace('"', '\\"')
        content += f'msgid "{escaped_eng}"\nmsgstr "{escaped_trans}"\n\n'
        
    with open(po_path, "w", encoding="utf-8") as f:
        f.write(content)

    # Compile .mo
    os.makedirs(mo_dir, exist_ok=True)
    subprocess.run(["msgfmt", po_path, "-o", mo_path], check=True)
    print(f"  [{lang}] OK")

